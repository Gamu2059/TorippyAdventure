void initializeItem(ID* id,int variety,int state,int direction){
	createItem(id,variety,getItemMoveNumber(id,variety,state),direction);
	switch(variety){
		case 0:
			id->eventFlag=FALSE;
			id->fallFlag=FALSE;
			break;
		default:
			id->eventFlag=FALSE;
			id->fallFlag=FALSE;
			break;
	}
	id->v0=0;
	id->vA=0;
	id->vX=0;
	id->fallTime=0;
	id->existFlag=TRUE;
}
void createItem(ID* id,int variety,int number,int direction){
	int y,x;
	for(y=0;y<ITEM_LINES;y++){
		for(x=0;x<ITEM_COLS;x++){
			if(direction==RIGHT)
				id->item[y][x]=itemInfo[variety][number][y][x];
			else if(direction==LEFT)
				id->item[y][x]=itemInfo[variety][number][y][ITEM_COLS-x-1];
		}
	}
	id->variety=variety;
	id->number=number;
	id->direction=direction;
}
int getItemMoveNumber(ID* id,int variety,int state){
	switch(variety){
		case 0:
			switch(state){
				case NO_ACTION:
					return 0;
				default:
					return 0;
			}
		default:
			return 0;
	}
}
